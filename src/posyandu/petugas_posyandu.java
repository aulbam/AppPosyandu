    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        String cari1=bcari.getText();
        int jumdata=0;
        removeTable();
        try{
            Statement stat = conn.createStatement();
            String sql ="SELECT * FROM petugas_posyandu where id_petugas like '%"+cari1+"%' or nama like '%"+cari1+"%' order by id_petugas asc";
            System.out.println(sql);

            ResultSet rs=stat.executeQuery(sql);
            while(rs.next()){
                String id_petugas=rs.getString("no_id");
                String nama=rs.getString("nama");
                String no_tlp=rs.getString("no_tlp");
                String alamat=rs.getString("alamat");
                String tugas=rs.getString("tugas");

                String data[]={id_petugas,nama,no_tlp,alamat,tugas};
                tabmode.addRow(data);
                jumdata++;
            }
        }catch(SQLException sqle){
            jumdata=0;
            JOptionPane.showMessageDialog(null,"Data gagal masuk tabel" +sqle);
        }
        lbljum.setText(jumdata+ "Data");
        // TODO add your handling code here:
    }//GEN-LAST:event_bcariActionPerformed

    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String sql = "insert into imunisasi values (?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement (sql);
            stat.setString (1,idpgs.getText());
            stat.setString (2,nama.getText());
            stat.setString (3,tlp.getText());
            stat.setString (4,almt.getText());
            stat.setString (5,tgs.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data berhasil disimpan");
            kosong();
            idpgs.requestFocus();
            datatable();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data gagal disimpan" +e);
        }
        autonumber();
        // TODO add your handling code here:
    }//GEN-LAST:event_bsimpanActionPerformed

