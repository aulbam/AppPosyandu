     protected void autonumber(){ 
        try{ 
            String sql = "SELECT id_petugas FROM imunisasi order by id_petugas asc";
            Statement stat = conn.createStatement();
            ResultSet rs=stat.executeQuery(sql);
            idpgs.setText ("Id0001");
             while(rs.next()){ 
                 String id_petugas = rs.getString("id_petugas").substring(2);
                 int nomor = Integer.parseInt (id_petugas) + 1;
                 String nol = "";
                  if (nomor<10) 
                  {nol = "000";} 
                  else if (nomor<100)
                  {nol = "00";} 
                  else if (nomor<1000)
                  {nol = "0";}
                  else if (nomor<10000)
                  {nol = "";}
     
                 idpgs.setText("Id"+ nol+nomor);
        }
        }
    
     catch (Exception e){ 
        JOptionPane.showMessageDialog(null,"Penomoran Salah" + e);         
    }
    }
     
     protected void datatable(){  
        removeTable();
        autonumber();
        int jumdata=0;
Object[] Baris ={"id_petugas","nama","no_tlp","alamat","tugas"};           
tabmode = new DefaultTableModel(null, Baris);           
tableimunisasi.setModel(tabmode);
    String sql ="select*from petugas_posyandu";
                      
try {               
java.sql.Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        while (hasil.next()){                    
                String id_petugas = hasil.getString("id_petugas");
                String nama = hasil.getString("nama");
                String no_tlp = hasil.getString("no_tlp");
                String alamat = hasil.getString("alamat");
                String tugas = hasil.getString("tugas");
               
                String [] data = {id_petugas,nama,no_tlp,alamat,tugas};
                tabmode.addRow(data);
                jumdata=jumdata+1;
            }
        }catch (SQLException sqle) {
            jumdata=0;
        JOptionPane.showMessageDialog(null,"Data gagal masuk ke tabel" +sqle);
    }
    lbljum.setText(jumdata+ "Data");
}
